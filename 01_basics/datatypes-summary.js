// Reference (Non primitive)

// Object, Array and Function

// https://262.ecma-international.org/5.1/#sec-11.4.3


/*
    Two types of memory
    1. Stack => used to store primitive data types, here you get the copy to work with
    2. Heap => used to store non primitive data types, here you get the reference to the memory

    Primitives like numbers and booleans are stored by value, while objects and 
    functions are stored by reference.
*/